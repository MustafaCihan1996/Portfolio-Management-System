    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        
        String a = jTextField3.getText();
        
        double b = 0;
        
        
        
        if(a.isEmpty())
        
        {
        
             JOptionPane.showMessageDialog(null, "Price can not be empty");  
             
            
        }
        else
        
        {
            
        
        try
        
        {
            
            b =Double.parseDouble(a);
            
            InvestmentAll.get(this.line).setPrice(b);
            
            JOptionPane.showMessageDialog(null, "Succesfully Uptaded");  
            
        }
        
        catch(NumberFormatException e)
        
        {
            
            JOptionPane.showMessageDialog(null, "Price should be integer");  
        
        }
        
        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

