    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                 
        if(this.line == 0)
        
        {
            
            jButton1.setEnabled(false);
            
            jTextField1.setText(InvestmentAll.get(0).getName());
            
            jTextField2.setText(InvestmentAll.get(0).getSymbol());
            
            
            
            
        }
        
        else
        
        {
            
        this.line--;
        
        jTextField1.setText(InvestmentAll.get(this.line).getName());
            
        jTextField2.setText(InvestmentAll.get(this.line).getSymbol());
            
        jTextField3.setText("");
        
        }
        
        
        
            jButton2.setEnabled(true);
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        
        jTextField1.setText("HAHA");
        
        if(line+1 == InvestmentAll.size())
        
        {
      
            jButton2.setEnabled(false);
            
            jTextField1.setText(InvestmentAll.get(InvestmentAll.size()-1).getName());
            
            jTextField2.setText(InvestmentAll.get(InvestmentAll.size()-1).getSymbol());
        
        }
        
        else
        
        {
            this.line++;
            
            jTextField1.setText(InvestmentAll.get(line).getName());
            
            jTextField2.setText(InvestmentAll.get(line).getSymbol());
            
            jTextField3.setText("");
            
        } 
        
         jButton1.setEnabled(true);
    }//GEN-LAST:event_jButton2ActionPerformed

